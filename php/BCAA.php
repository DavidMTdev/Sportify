        <h2>Caracteristiques</h2>
        <div class="border"></div>

        <p>Contrairement aux autres acides aminés qui sont métabolisés dans le foie,
            les BCAA sont métabolisés directement dans le muscle (ils sont oxydés dans les mitochondries musculaires).
            <br>
            <br>
            Lorsque les muscles ont épuisé leur stock de glycogène (sucre),
            il commence à se servir des lipides et des protéines pour produire de l’énergie.
            De grosses protéines sont ainsi détruites en plusieurs petits morceaux afin de récupérer les BCAA,
            qui seront utilisés comme carburant.
        </p>
    </div>
</div>

<div class="supplement-type">
