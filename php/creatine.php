<div class="supplement-description">

    <div class="description">
        <h2>Description</h2>
        <div class="border"></div>
        <p>La créatine est un dérivé de 3 acides aminés qui sont l’arginine, la glycine et la méthionine. C’est
            donc, en principe, un supplément à base d’acides animés et non pas un acide aminé à proprement dit.
            Son rôle est de fournir de l’énergie aux cellules pour les efforts courts et intenses. La créatine
            est produite naturellement par le corps et est stockée principalement dans les muscles
            squelettiques.
            <br><br>
            La supplémentation augmente de 10 à 30% le contenu du muscle en créatine et en phosphocréatine, ce
            qui permet d’améliorer la disponibilité d’énergie pour des exercices de forte intensité.
            <br><br>
            Notre corps utilise aussi de la créatine apportée par l’alimentation : on en retrouve dans les
            viandes et les poissons (un steak de 150 g contient environ 1 g de créatine par exemple).
        </p>
    </div>
</div>

<div class="supplement-goal">
