<div class="supplement-type">
    <div class="type">
        <h2>Dangers de la créatine</h2>
        <div class="border"></div>
        <p>La prise de créatine ne présente aucun danger si elle est prise dans les quantités recommandées.
            Surdosée, elle peut entraîner des crampes musculaires, des crampes abdominales, des diarrhées et une
            déshydratation.
            <br><br>
            La prise de créatine entraînant une augmentation des déchets métaboliques, veillez à resté hydraté
            pour faciliter l’élimination au niveau des reins.
            <br><br>
            Concernant la fonction rénale, de nombreuses études montrent que la supplémentation en créatine (de
            5 à 30 g/jour) n’a pas d’effet néfaste sur les reins chez des personnes en bonne santé.</p>
